// It is called at the end of the new.html document which executes this function on page load. It shows the section for user input, hides the readonly JSON export field at the bottom of the page, creates event listener which on user input, unhides the export field, and should the user click in the export field it will highlight all content for copying and pasting outside the page.  Repeat for other text input boxes.  Finally, when the user clicks the submit button, the JSON content is sent to the server via the articleView.submit method.

articleView.initNewArticlePage = () => {
  $('.tab-content').show();
  $('#export-field').hide();
  $('#article-json').on('focus', function(){
    this.select();
  });

  $('#new-form').on('change', 'input, textarea', articleView.create);
  $('#new-form').on('submit', articleView.submit);
};

// COMMENT: When is this function called? What event ultimately triggers its execution?

// As part of the above initNewArticlePage method, this method clears content from the DOM at the articles id, then passes inputs through the constructor function, converts to HTML, writes to DOM (if necessary highlights any code syntax), unhides the export field, and then stringifies the HTML to display in that field.

articleView.create = () => {
  let article;
  $('#articles').empty();

  article = new Article({
    title: $('#article-title').val(),
    author: $('#article-author').val(),
    authorUrl: $('#article-author-url').val(),
    category: $('#article-category').val(),
    body: $('#article-body').val(),
    publishedOn: $('#article-published:checked').length ? new Date() : null
  });

  $('#articles').append(article.toHtml());

  $('pre code').each(function(i, block) {
    hljs.highlightBlock(block);
  });

  $('#export-field').show();
  $('#article-json').val(`${JSON.stringify(article)},`);
};

// COMMENT: When is this function called? What event ultimately triggers its execution?
// When the user submits the form, the method prevents page refresh, then passes the event through the constructor function.  It will verify that if the "Published" box is checked, then it will add the current date.

articleView.submit = event => {
  event.preventDefault();
  let article = new Article({
    title: $('#article-title').val(),
    author: $('#article-author').val(),
    authorUrl: $('#article-author-url').val(),
    category: $('#article-category').val(),
    body: $('#article-body').val(),
    publishedOn: $('#article-published:checked').length ? new Date() : null
  });

  // COMMENT: Where is this function defined? When is this function called? What event ultimately triggers its execution?
  // It is defined on articles.js and is called every time the user hits the submit button on new.html.
  article.insertRecord();
}

articleView.initIndexPage = () => {
  Article.all.forEach(article =>{
    $('#articles').append(article.toHtml())
  });

  articleView.populateFilters();
  articleView.handleCategoryFilter();
  articleView.handleAuthorFilter();
  articleView.handleMainNav();
  articleView.setTeasers();
};
